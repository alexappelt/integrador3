package org.prog2.model;

import java.util.List;


public class Comanda{

    String codigo;
    List<Produto> itens;


    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public List<Produto> getItens() {
        return itens;
    }

    public void setItens(List<Produto> itens) {
        this.itens = itens;
    }









}
